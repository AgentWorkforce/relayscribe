#!/usr/bin/env node
// Regenerates the brand-specific artifacts from a brand config:
//   - Relayscribe/Sources/Brand.swift             (in-app product strings)
//   - Relayscribe/AppBundle/Info.plist            (display name, bundle id, consent copy)
//   - Relayscribe/AppBundle/Resources/icon.icns   (rasterized brand icon)
//   - branding/.generated/<id>/build.env            (APP_NAME etc. for the release workflow)
//
// The relayscribe base ships with the default brand already applied, so the
// app builds without running this. White-label builds (e.g. a downstream overlay)
// run `apply-branding --brand-file <path-to-brand>.json` before `swift build`.
//
// It NEVER edits recorder logic or the sidecar — only presentation.
import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), '../..');

const REQUIRED = ['id', 'productName', 'shortName', 'appId', 'supportUrl', 'icon', 'consent'];
const CONSENT_KEYS = ['microphone', 'screenCapture', 'appleEvents'];

export function loadBrand(brandFile) {
  const brand = JSON.parse(readFileSync(brandFile, 'utf8'));
  const missing = REQUIRED.filter((k) => !brand[k]);
  if (missing.length) throw new Error(`missing field(s): ${missing.join(', ')}`);
  for (const k of CONSENT_KEYS) {
    if (!brand.consent[k]) throw new Error(`missing consent.${k}`);
  }
  if (!/^[a-z0-9.-]+$/.test(brand.appId)) throw new Error(`appId must be reverse-DNS: ${brand.appId}`);
  if (!/^[a-z0-9][a-z0-9-]*$/.test(brand.id)) throw new Error(`id must be kebab-case: ${brand.id}`);
  return brand;
}

function xmlEscape(value) {
  return value.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function setPlistString(plist, key, value) {
  const re = new RegExp(`(<key>${key}</key>\\s*<string>)[\\s\\S]*?(</string>)`);
  if (!re.test(plist)) throw new Error(`Info.plist: <key>${key}</key> not found`);
  return plist.replace(re, `$1${xmlEscape(value)}$2`);
}

function brandSwift(brand) {
  return `// Generated by branding/lib/apply-branding.mjs — do not edit by hand.
//
// Active brand: ${brand.id}. Regenerate with: npm run branding:apply -- --brand <id>
enum Brand {
    static let productName = ${JSON.stringify(brand.productName)}
    static let shortName = ${JSON.stringify(brand.shortName)}
    static let bundleIdentifier = ${JSON.stringify(brand.appId)}
    static let supportURL = ${JSON.stringify(brand.supportUrl)}
}
`;
}

export function applyBrand(brandFile) {
  const brand = loadBrand(brandFile);
  const brandDir = dirname(brandFile);

  const iconSvg = resolve(brandDir, brand.icon);
  if (!existsSync(iconSvg)) throw new Error(`icon not found: ${brand.icon} (resolved ${iconSvg})`);

  // 1. Brand.swift
  writeFileSync(join(rootDir, 'Relayscribe/Sources/Brand.swift'), brandSwift(brand));

  // 2. Info.plist (display name, bundle id, consent copy). Executable/bundle name
  //    stay "Relayscribe" — that is the binary SwiftPM builds for every brand.
  const plistPath = join(rootDir, 'Relayscribe/AppBundle/Info.plist');
  let plist = readFileSync(plistPath, 'utf8');
  plist = setPlistString(plist, 'CFBundleDisplayName', brand.productName);
  plist = setPlistString(plist, 'CFBundleIdentifier', brand.appId);
  plist = setPlistString(plist, 'NSMicrophoneUsageDescription', brand.consent.microphone);
  plist = setPlistString(plist, 'NSScreenCaptureUsageDescription', brand.consent.screenCapture);
  plist = setPlistString(plist, 'NSAppleEventsUsageDescription', brand.consent.appleEvents);
  writeFileSync(plistPath, plist);

  // 3. icon.icns — reuse the icon pipeline with the brand's SVG
  execFileSync(process.execPath, [join(rootDir, 'scripts/generate-macos-icons.mjs')], {
    stdio: 'inherit',
    env: { ...process.env, BRAND_ICON_SVG: iconSvg },
  });

  // 4. build.env — consumed by the release workflow (the .app/DMG names per brand)
  const genDir = join(rootDir, 'branding/.generated', brand.id);
  mkdirSync(genDir, { recursive: true });
  const dmgName = `${brand.productName.replace(/[^A-Za-z0-9]+/g, '')}.dmg`;
  writeFileSync(
    join(genDir, 'build.env'),
    [
      `APP_NAME=${brand.productName}`,
      `APP_EXECUTABLE=Relayscribe`,
      `BUNDLE_ID=${brand.appId}`,
      `DMG_NAME=${dmgName}`,
      '',
    ].join('\n'),
  );

  return { brand, genDir, dmgName };
}

function parseArgs(argv) {
  const args = { brand: 'agent-relay', brandFile: null };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === '--brand') args.brand = argv[++i];
    else if (argv[i] === '--brand-file') args.brandFile = argv[++i];
  }
  return args;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const brandFile = args.brandFile
    ? resolve(args.brandFile)
    : join(rootDir, 'branding/brands', `${args.brand}.json`);
  if (!existsSync(brandFile)) {
    console.error(`brand config not found: ${brandFile}`);
    process.exit(1);
  }
  try {
    const { brand, genDir, dmgName } = applyBrand(brandFile);
    console.log(`Applied brand "${brand.id}" → ${brand.productName} (${brand.appId})`);
    console.log(`  Brand.swift + Info.plist + icon.icns updated`);
    console.log(`  build.env → ${join(genDir, 'build.env')} (DMG: ${dmgName})`);
  } catch (err) {
    console.error(`apply-branding failed for ${brandFile}: ${err.message}`);
    process.exit(1);
  }
}

if (resolve(process.argv[1]) === fileURLToPath(import.meta.url)) main();
